######################################### String Slicing #############################################################################
b = "CLOUDQUICKPOCS"
# b[0] =  C , b[1] =  L b[2] =  O .....
print(type(b))
print(b[2:5])

##  Slice From the Start
b = "CLOUDQUICKPOCS"
print(b[:5])

## Slice To the End
b = "CLOUDQUICKPOCS"
print(b[2:])

## Negative Indexing
b = "CLOUDQUICKPOCS"
print(b[-5:-4])