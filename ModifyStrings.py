## upper case:
a = "cloudquickPOCs"
print(a.upper())

## lower case:
a = "CLOUDQUICKPOCS"
print(a.lower())

## Remove Whitespace
a = " CLOUDQUICKPOCS "
print(a.strip()) 

## Replace String
a = "CLOUDQUICKPOCS"
print(a.replace("C", "J"))

## Split String
a = "Hello, CLOUDQUICKPOCS"
print(a.split(",")) # returns ['Hello', 'CLOUDQUICKPOCS']
