## upper case:
a = "Hi, There!"
print(a.upper())

## lower case:
a = "Hi, There!"
print(a.lower())

## Remove Whitespace
a = " Hi, There! "
print(a.strip()) 

## Replace String
a = "Hi, There!"
print(a.replace("H", "J"))

## Split String
a = "Hi, There!"
print(a.split(",")) # returns ['Hi', ' There!']

